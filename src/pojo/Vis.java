package pojo;

import java.sql.Date;

public class Vis {
	Date visd;
	String cname;
	String vid;
	Vis(){
		
	}
	public Date getVisd() {
		return visd;
	}
	public void setVisd(Date visd) {
		this.visd = visd;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getVid() {
		return vid;
	}
	public void setVid(String vid) {
		this.vid = vid;
	}
	
}
